package ch02.sec04;

public class DoubleAndFloat {
	public static void main(String[] args) {
         double doubleNumber1 =3.14;
         double doubleNumber2 =314E-2;  //314.0*10-²
         
         System.out.println(doubleNumber1);
         System.out.println(doubleNumber2);
         
         float floatNumber1 = 3.14F;
         float floatNumber2 = 3E4F;  //3.0*10⁴
         
         System.out.println(floatNumber1);
         System.out.println(floatNumber2);
	} 
}
